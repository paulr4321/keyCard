package java;

public class User {

    private int id;
    private String name;


    public int getId(){
        return this.id;
    }

    public String getName(){
        return this.name;
    }

    public void setId(int idIn){
        this.id = idIn;
    }

    public void setName(String nameIn){
        this.name = nameIn;
    }
}
